#Exercicio5: Calculo do perímetro e área de um retangulo 

base = float(input('Digite a base do retangulo em cm: '))
altura = float(input('Digite a altura do retangulo em cm: '))

per = (base + altura) * 2
area = base * altura 

print('O perimetro do retangulo é de: ', per, 'cm  ', 'A area do retangulo é de: ', area, 'cm')